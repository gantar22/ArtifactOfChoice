    @Override
    public void receivePostUpdate() {
        cardReplacement.receivePostUpdate();
        if(CardCrawlGame.startOver && CardCrawlGame.screenTimer >= 2.0f)
        {
            if(relicReplacement != null)
            {
                relicReplacement.onShutdown();
            }
            if(potionReplacement != null)
            {
                potionReplacement.onShutdown();
            }
        }
        else
        {
            if(relicReplacement != null)
            {
                relicReplacement.receivePostUpdate();
            }

            if(potionReplacement != null)
            {
                potionReplacement.receivePostUpdate();
            }
        }
    }

    @Override
    public void receiveRender(SpriteBatch spriteBatch) {
        if(AbstractDungeon.screen == AbstractDungeon.CurrentScreen.NONE || AbstractDungeon.screen == AbstractDungeon.CurrentScreen.COMBAT_REWARD)
        {
            if(relicReplacement != null)
                relicReplacement.render(spriteBatch);

            if(potionReplacement != null)
                potionReplacement.render(spriteBatch);
        }

    }

    public void receivePostDeath()
    {
        if(relicReplacement != null)
        {
            relicReplacement.onShutdown();
        }
        if(potionReplacement != null)
        {
            potionReplacement.onShutdown();
        }
    }
